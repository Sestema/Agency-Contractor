        public static IReadOnlyList<ProgramHelpTopic> Search(string query, int limit = 4)
        {
            if (string.IsNullOrWhiteSpace(query))
                return Topics.Take(Math.Max(1, limit)).ToList();

            var normalizedQuery = Normalize(query);
            var tokens = normalizedQuery
                .Split(' ', StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries)
                .Distinct(StringComparer.Ordinal)
                .ToArray();

            return Topics
                .Select(topic => new
                {
                    Topic = topic,
                    Score = ScoreTopic(topic, normalizedQuery, tokens)
                })
                .Where(x => x.Score > 0)
                .OrderByDescending(x => x.Score)
                .ThenBy(x => x.Topic.Title, StringComparer.OrdinalIgnoreCase)
                .Take(Math.Max(1, limit))
                .Select(x => x.Topic)
                .ToList();
        }

        private static int ScoreTopic(ProgramHelpTopic topic, string normalizedQuery, IReadOnlyList<string> tokens)
        {
            var score = 0;
            score += ScoreText(topic.Title, normalizedQuery, tokens, 6);
            score += ScoreText(topic.Summary, normalizedQuery, tokens, 3);
            score += ScoreText(topic.WhenToUse, normalizedQuery, tokens, 3);
            score += ScoreText(topic.HowToOpen, normalizedQuery, tokens, 2);

            foreach (var value in topic.Keywords)
                score += ScoreText(value, normalizedQuery, tokens, 5);

            foreach (var value in topic.CommonTasks)
                score += ScoreText(value, normalizedQuery, tokens, 2);

            foreach (var value in topic.RelatedTopics)
                score += ScoreText(value, normalizedQuery, tokens, 1);

            return score;
        }

        private static int ScoreText(string value, string normalizedQuery, IReadOnlyList<string> tokens, int weight)
        {
            if (string.IsNullOrWhiteSpace(value))
                return 0;

            var normalizedValue = Normalize(value);
            var score = 0;

            if (normalizedValue.Contains(normalizedQuery, StringComparison.Ordinal))
                score += weight * 3;

            foreach (var token in tokens)
            {
                if (token.Length < 2)
                    continue;

                if (normalizedValue.Contains(token, StringComparison.Ordinal))
                    score += weight;
            }

            return score;
        }

        private static string Normalize(string value)
        {
            var builder = new StringBuilder(value.Length);
            foreach (var ch in value.ToLowerInvariant())
                builder.Append(char.IsLetterOrDigit(ch) ? ch : ' ');

            return string.Join(' ', builder.ToString()
                .Split(' ', StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries));
        }
    }
